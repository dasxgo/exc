## Calculate lambda funtion

suma = lambda a,b,c: a + b + c
print(f'resultado suma =>', suma(10,20,30))

resta = lambda a,b: b - a
print(f'resultado resta =>', resta(10,20))

multiplicacion = lambda a,b,c: a * b * c
print(f'resultado multiplicacion=>', multiplicacion(5,16,12))

division = lambda a,b: b / a
print(f'resultado division =>', division(2, 50))